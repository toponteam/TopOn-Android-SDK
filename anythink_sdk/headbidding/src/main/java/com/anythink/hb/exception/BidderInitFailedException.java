package com.anythink.hb.exception;

public class BidderInitFailedException extends Exception {

	public BidderInitFailedException() {
		super();
	}

	public BidderInitFailedException(String arg0) {
		super(arg0);
	}

	public BidderInitFailedException(Throwable arg0) {
		super(arg0);
	}

	public BidderInitFailedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
