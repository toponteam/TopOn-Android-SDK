package com.anythink.hb.exception;

public class BidRequestException extends Exception {

	public BidRequestException() {
	}

	public BidRequestException(String arg0) {
		super(arg0);
	}

	public BidRequestException(Throwable arg0) {
		super(arg0);
	}

	public BidRequestException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
